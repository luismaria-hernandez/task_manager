
package Model;

public class PrioridadesFix 
{
    private int idPrioridadFix;
    private String prioridadFix;

    public PrioridadesFix(int idPrioridadFix, String prioridadFix) {
        this.idPrioridadFix = idPrioridadFix;
        this.prioridadFix = prioridadFix;
    }

    public int getIdPrioridadFix() {
        return idPrioridadFix;
    }

    public void setIdPrioridadFix(int idPrioridadFix) {
        this.idPrioridadFix = idPrioridadFix;
    }

    public String getPrioridadFix() {
        return prioridadFix;
    }

    public void setPrioridadFix(String prioridadFix) {
        this.prioridadFix = prioridadFix;
    }
    
    
}
