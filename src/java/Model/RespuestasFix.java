
package Model;

public class RespuestasFix 
{
    private int idRespFix;
    private String respFix;

    public RespuestasFix(int idRespFix, String respFix) {
        this.idRespFix = idRespFix;
        this.respFix = respFix;
    }

    public int getIdRespFix() {
        return idRespFix;
    }

    public void setIdRespFix(int idRespFix) {
        this.idRespFix = idRespFix;
    }

    public String getRespFix() {
        return respFix;
    }

    public void setRespFix(String respFix) {
        this.respFix = respFix;
    }

    

}
